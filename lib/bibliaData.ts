export function getCapitulosPorLivro(livro: string): number {
  return CAPITULOS_POR_LIVRO[livro] ?? 1
}

export function getVersosPorCapitulo(livro: string, capitulo: number): number {
  const versos = VERSOS_POR_CAPITULO[livro]
  if (!versos || capitulo < 1 || capitulo > versos.length) return 1
  return versos[capitulo - 1]
}

